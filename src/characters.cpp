char getID3x5(char letter){
    char out = 0;
    if((letter >= 48) && (letter <= 58)){
        out = letter - 45;
    }
    if((letter >= 65) && (letter <= 90)){
        out = letter - 52;
    }
    if((letter >= 97) && (letter <= 122)){
        out = letter - 58;
    }
    else if((letter == 32) | (letter == 33)){
        out = letter - 32;
    }
    else if(letter == 46){
        out = 2;
    }
    // else letter is not in range, will return ID for a space
    return out;
}

